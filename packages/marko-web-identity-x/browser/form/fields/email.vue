<template>
  <form-group>
    <form-label :for="id" :required="true">
      {{ translate("emailLabel") }}
    </form-label>
    <input
      :id="id"
      v-model="email"
      class="form-control"
      type="email"
      :required="true"
      :disabled="disabled"
      :placeholder="placeholder"
      autocomplete="email"
    >
  </form-group>
</template>

<script>
import elementId from '@parameter1/base-cms-marko-web/browser/components/element-id';
import FormGroup from '../common/form-group.vue';
import FormLabel from '../common/form-label.vue';
import i18n from '../../../i18n';

export default {
  components: {
    FormGroup,
    FormLabel,
  },
  props: {
    disabled: {
      type: Boolean,
      default: false,
    },
    label: {
      type: String,
      default: '',
    },
    placeholder: {
      type: String,
      default: '',
    },
    value: {
      type: String,
      default: '',
    },
    lang: {
      type: String,
      default: 'en',
    },
  },
  computed: {
    id() {
      return elementId('identity-x-email-field');
    },
    email: {
      get() {
        return this.value || '';
      },
      set(email) {
        this.$emit('input', email || null);
      },
    },
  },
  methods: {
    translate(key) {
      if (this.label) return this.label;
      return i18n(this.lang, key);
    },
  },
};
</script>
